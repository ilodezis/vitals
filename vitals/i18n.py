def t(translation_key: str, **kwargs: object) -> str:
    """Look up a translation key for the current language.

    Fallback chain: current language → ``"en"`` → raw key.
    Keyword arguments are passed to ``str.format()`` if present.
    """
    lang = current_lang.get()
    text = STRINGS.get(lang, _EN).get(translation_key)
    if text is None:
        text = _EN.get(translation_key, translation_key)
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError, IndexError):
            return text
    return text


def get_js_strings(lang: str | None = None) -> dict[str, str]:
    """Return the ``js.*`` slice for injection into ``window.__STRINGS__``.

    Keys are stripped of the ``js.`` prefix so JS code uses ``t("saving")``
    instead of ``t("js.saving")``.
    """
    if lang is None:
        lang = current_lang.get()
    source = STRINGS.get(lang, _EN)
    return {k[3:]: v for k, v in source.items() if k.startswith("js.")}


def plural(n: int | float, one: str, few_or_other: str, many: str | None = None) -> str:
    """Language-aware plural.  EN uses 2 forms, RU uses 3."""
    try:
        n = abs(int(n))
    except (TypeError, ValueError):
        return many if many is not None else few_or_other
    lang = current_lang.get()
    if lang == "ru" and many is not None:
        if n % 10 == 1 and n % 100 != 11:
            return one
        if 2 <= n % 10 <= 4 and not (12 <= n % 100 <= 14):
            return few_or_other
        return many
    return one if n == 1 else few_or_other
